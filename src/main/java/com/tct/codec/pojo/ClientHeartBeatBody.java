package com.tct.codec.pojo;

public class ClientHeartBeatBody{
	
	private String reserve;
	private String bluetoothMac;
	private String lo;
	private String la;
	private String areaCode;
	private String batteryPower;
	private String deviceBatteryPower;
	private String authCode;
	
	public String getReserve() {
		return reserve;
	}
	public void setReserve(String reserve) {
		this.reserve = reserve;
	}
	public String getBluetoothMac() {
		return bluetoothMac;
	}
	public void setBluetoothMac(String bluetoothMac) {
		this.bluetoothMac = bluetoothMac;
	}
	public String getLo() {
		return lo;
	}
	public void setLo(String lo) {
		this.lo = lo;
	}
	public String getLa() {
		return la;
	}
	public void setLa(String la) {
		this.la = la;
	}
	public String getAreaCode() {
		return areaCode;
	}
	public void setAreaCode(String areaCode) {
		this.areaCode = areaCode;
	}
	public String getBatteryPower() {
		return batteryPower;
	}
	public void setBatteryPower(String batteryPower) {
		this.batteryPower = batteryPower;
	}
	public String getDeviceBatteryPower() {
		return deviceBatteryPower;
	}
	public void setDeviceBatteryPower(String deviceBatteryPower) {
		this.deviceBatteryPower = deviceBatteryPower;
	}
	public String getAuthCode() {
		return authCode;
	}
	public void setAuthCode(String authCode) {
		this.authCode = authCode;
	}
	
	
}
